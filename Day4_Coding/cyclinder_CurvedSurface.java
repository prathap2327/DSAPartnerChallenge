package Day4_Coding;

public class cyclinder_CurvedSurface {
    public static void main(String[] args) {
        double radius = 3.0;
        double height = 5.0;
        double curvedSurfaceArea = 2 * Math.PI * radius * height;
        System.out.printf("Curved surface area of the cylinder: %.2f", curvedSurfaceArea);
    }
}
